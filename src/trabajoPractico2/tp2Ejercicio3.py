from math import pi

r = int(input("Ingrese la longitud del radio del circulo: "))

print("La superficie del circulo es igual a: ", (pi * r ** 2))

print("El perímetro de la circunferencia es igual a: ", (pi * (r * 2)))

print("La superficie de la esfera es igual a: ", (4 * pi * r ** 2))

print("El volúmen de la esfera es igual a: ", (4/3 * pi * r ** 3))

